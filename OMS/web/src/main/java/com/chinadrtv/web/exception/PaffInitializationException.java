package com.chinadrtv.web.exception;

public class PaffInitializationException extends RuntimeException {

    private static final long serialVersionUID = -9089153626262181758L;

	public PaffInitializationException() {
		super();
	}

	public PaffInitializationException(String message, Throwable cause) {
		super(message, cause);
	}

	public PaffInitializationException(String message) {
		super(message);
	}

	public PaffInitializationException(Throwable cause) {
		super(cause);
	}

}
